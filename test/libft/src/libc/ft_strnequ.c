#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (!s1 || !s2)
		return (0);
	if (*s2 == '\0')
		return (1);
	i = -1;
	while (*(s1 + ++i) && *(s2 + i) && i < n - 1)
		if (*(s1 + i) != *(s2 + i))
			return (0);
	return ((*(s1 + i) == *(s2 + i)));
}
