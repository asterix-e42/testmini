#include <string.h>

char	*ft_strstr(char *s1, char *s2)
{
	int		i;
	int		j;

	if (!*(s2))
		return (s1);
	i = -1;
	while (*(s1 + ++i))
	{
		j = 0;
		while (*(s2 + j) && (*(s1 + i + j) == *(s2 + j)))
			j++;
		if (!(*(s2 + j)))
			return (s1 + i);
	}
	return (NULL);
}
