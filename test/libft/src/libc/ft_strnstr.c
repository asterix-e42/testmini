#include <string.h>

char	*ft_strnstr(char *s1, char *s2, size_t n)
{
	size_t	i;
	size_t	j;

	if (!*(s2))
		return (s1);
	i = -1;
	while (*(s1 + ++i) && (i < n))
	{
		j = 0;
		while (*(s2 + j) && (*(s1 + i + j) == *(s2 + j)) && (i + j < n))
			j++;
		if (!(*(s2 + j)))
			return (s1 + i);
	}
	return (NULL);
}
